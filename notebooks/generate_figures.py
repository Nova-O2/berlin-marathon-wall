    figure_a_density(df)

    print("\n=== Generating Figure B (prevalence with Wilson 95% CI) ===")
    figure_b_prevalence(df)

    print("\n=== Generating Figure C (stratified with 95% CI of mean) ===")
    figure_c_stratified(df)

    print("\nFigures D and E are generated by their analysis scripts:")
    print("  - notebooks/r1_pacing_fine_grained.py → Figure_D_Pacing_Variability")
